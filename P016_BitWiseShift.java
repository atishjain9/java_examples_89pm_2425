class P016_BitWiseShift 
{
	public static void main(String[] args) 
	{
		int a=25;
		System.out.println("Bitwise >>2 !:"+(a>>2));
		a=10;
		System.out.println("Binary of 10 is:"+Integer.toBinaryString(a));
		System.out.println("Bitwise <<2 !:"+(a<<2));

		a=-5;
		System.out.println("Bitwise >>5!:"+(a>>>2));



	}
}
