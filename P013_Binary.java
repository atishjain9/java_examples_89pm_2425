class P013_Binary 
{
	public static void main(String[] args) 
	{
		int no=25;
		System.out.println("Binary of 25 is:"+Integer.toBinaryString(no));
		System.out.println("Binary of 10 is:"+Integer.toBinaryString(10));
		System.out.println("Binary of 5 is:"+Integer.toBinaryString(5));
		System.out.println("Binary of -5 is:"+Integer.toBinaryString(-5));


		
	}
}
